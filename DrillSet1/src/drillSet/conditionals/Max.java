package drillSet.conditionals;

/**
 *
 * @author Rene Gomez
 */
public class Max {

    public int Max(int a, int b, int c) {
        if (a > b && a > c) {
            return a;
        } else if (b > a && b > c) {
            return b;
        } else if (c > b && c > a) {
            return c;
        } else {
            return -1;
        }
    }

}
